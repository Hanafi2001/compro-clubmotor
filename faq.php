                                    <div class="accordion-content">
                                        <p>Wingday adalah kegiatan touring bersama keluarga besar HTCI yang diagendakan dan ditetapkan oleh PP HTCI, baik susunan acara maupun rute yang akan dilalui. Wingday di laksanakan 4 tahun sekali. Sejak pertama dilaksanakan pada tahun 2008 lalu, sudah dilaksanakan 3 kali kegiatan WINGDAY.</p>
