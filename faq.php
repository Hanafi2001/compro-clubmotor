                                    </a>

                                    <div class="accordion-content">
                                        <p>JAMNAS atau Jambore Nasional adalah kegiatan 2 tahunan berkumpulnya keluarga besar HTCI di suatu tempat yang diputuskan ditetapkan oleh Pengurus Pusat HTCI guna mempererat silaturahmi antar anggota yang mencapai ribuan orang. Sampai saat ini, HTCI telah melaksanakan 8 kali kegiatan JAMNAS sejak di bentuk 2004 lalu.</p>
                                    </div>
                                </li>
                                <li class="accordion-item">
