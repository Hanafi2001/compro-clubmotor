                                    <div class="accordion-content">
                                        <p>MUNAS atau Musyawarah Nasional adalah kegiatan pemilihan Ketua Umum HTCI yang dihadiri oleh seluruh PENGDA HTCI se Indonesia untuk menentukan calon Ketua Umum HTCI dimasa akan datang. Kegiatan ini juga melibatkan Dewan Penasehat HTCI.</p>
                                    </div>
                                </li>

                                <li class="accordion-item">
                                    <a class="accordion-title" href="javascript:void(0)">
