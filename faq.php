                                    </a>

                                    <div class="accordion-content">
                                        <p>MUKERNAS atau Musyawarah Kerja Nasional adalah kegiatan rapat dan koordinasi yang dihadiri oleh Pengurus Pusat HTCI dan Pengurus Daerah HTCI untuk membahas regulasi maupun kebijakan-kebijakan yang dibuat oleh PP HTCI. Serta membahas beberapa hal yang sekiranya perlu disampaikan oleh Pengda HTCI pada PP HTCI, Selain itu Program Kerja Organisasi juga dibahas pada MUKERNAS ini.</p>
